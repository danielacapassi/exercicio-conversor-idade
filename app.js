
// Perguntar a idade  
var age = parseInt (prompt ("Qual é a sua idade?")); 
// Calcular idade em segundos
var ageInSeconds = age * 365 * 24 * 60 * 60;
// Mostrar a idade na console
console.log (ageInSeconds)
document.write ("Em seus " +  age  + " anos de idade, já se passaram " + ageInSeconds+ " segundos")


